public class Qst1 {
    public static void main(String[] args) {
        int n = 10; // limite dos numeros
        
        System.out.println("Numeros de 1 a " + n + ":"); // + é o operador de concatenação de strings
        for (int i=1; i<=n; i++){
            System.out.println(i);
        };
    }
}
